// All player-facing strings, four languages. `type Strings = typeof EN`
// means every locale is compile-checked for completeness — a missing key
// is a build error, never a runtime hole.
//
// Gabriel validates pt-BR; es/fr are machine-honest but reviewable.

export type Lang = 'en' | 'pt' | 'es' | 'fr'
export const LANGS: Lang[] = ['en', 'pt', 'es', 'fr']
export const LANG_LABELS: Record<Lang, string> = { en: 'ENGLISH', pt: 'PORTUGUÊS', es: 'ESPAÑOL', fr: 'FRANÇAIS' }

const KEY = 'jit-lang-v1'
let lang: Lang = 'en'
try {
  const s = localStorage.getItem(KEY)
  if (s && (LANGS as string[]).includes(s)) lang = s as Lang
} catch { /* default en */ }

export function currentLang(): Lang { return lang }
export function hasChosenLang(): boolean {
  try { return localStorage.getItem(KEY) !== null } catch { return true }
}
export function setLang(l: Lang) {
  lang = l
  try { localStorage.setItem(KEY, l) } catch { /* ok */ }
}
export function cycleLang(): Lang {
  setLang(LANGS[(LANGS.indexOf(lang) + 1) % LANGS.length])
  return lang
}

